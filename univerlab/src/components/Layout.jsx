/**
 * =============================================================================
 * Layout.jsx - 페이지 공통 레이아웃 컴포넌트
 * =============================================================================
 * 
 * 모든 페이지에 공통으로 적용되는 레이아웃입니다.
 * Header(상단 메뉴)와 Footer(하단 정보)를 포함합니다.
 * 
 * 📌 구조:
 * ┌─────────────────────────────┐
 * │         Header              │  ← 상단 메뉴, 로고
 * ├─────────────────────────────┤
 * │                             │
 * │         main                │  ← 각 페이지 내용 (children)
 * │       (children)            │
 * │                             │
 * ├─────────────────────────────┤
 * │         Footer              │  ← 하단 정보, 연락처
 * └─────────────────────────────┘
 * 
 * 📌 사용법 (App.jsx에서):
 * <Layout>
 *   <HomePage />   ← 이 부분이 children으로 전달됨
 * </Layout>
 * 
 * =============================================================================
 */

// Header 컴포넌트 - 상단 네비게이션 메뉴
import Header from './Header'

// Footer 컴포넌트 - 하단 정보 영역
import Footer from './Footer'

/**
 * Layout 컴포넌트
 * 
 * @param {React.ReactNode} children - 각 페이지의 내용 (Home, About 등)
 * 
 * 모든 페이지를 감싸서 동일한 Header와 Footer를 적용합니다.
 */
function Layout({ children }) {
  return (
    <div className="layout">
      {/* 상단 네비게이션 - 모든 페이지에서 동일하게 표시 */}
      <Header />
      
      {/* 메인 콘텐츠 영역 - 각 페이지마다 다른 내용이 표시됨 */}
      <main>{children}</main>
      
      {/* 하단 정보 - 모든 페이지에서 동일하게 표시 */}
      <Footer />
    </div>
  )
}

// 다른 파일에서 사용할 수 있도록 내보내기
export default Layout

